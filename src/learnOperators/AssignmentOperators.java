package learnOperators;

public class AssignmentOperators {

	int age = 10;
	int x;
	void addequalto() {
		age += 5;
		System.out.println("age =" +age);
	}
	void minusequalto() {
		age -= 3;
		System.out.println("age =" +age);
	}
	void multiplicationequalto() {
		x *= x;
		System.out.println("square is" +x);
	}
}
